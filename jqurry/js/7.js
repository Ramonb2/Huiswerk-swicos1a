$(document).ready(function(){
  $("#1 > img").click(function(){
    $("#1 > img").animate({
      height:'640px',
      width:'560px'
    });
  });
});
$(document).ready(function(){
  $("#1 > img").dblclick(function(){
    $("#1 > img").animate({
		height:'450px',
		width:'850px'
    });
  });
});
